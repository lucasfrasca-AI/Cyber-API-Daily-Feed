// SECTION 9 — MITRE ATT&CK via TAXII 2.1
// ============================================================
function fetchMitreAttack(body) {
  appendSectionHeader(body, '9 · MITRE ATT&CK — Tactics, Techniques & Procedures');

  const baseUrl      = 'https://attack-taxii.mitre.org/api/v21/';
  const taxiiHeaders = { 'Accept': 'application/taxii+json;version=2.1' };

  const collectionsRaw = safeGet(baseUrl + 'collections/', taxiiHeaders);
  if (!collectionsRaw) { appendItem(body, '⚠ Could not reach MITRE TAXII server.'); return; }

  let collections;
  try { collections = JSON.parse(collectionsRaw); } catch(e) {
    appendItem(body, '⚠ MITRE parse error: ' + e.message); return;
  }

  const enterprise = (collections.collections || []).find(function(c) {
    return c.title && c.title.toLowerCase().includes('enterprise');
  });

  if (!enterprise) { appendItem(body, '⚠ Enterprise ATT&CK collection not found.'); return; }
  appendItem(body, 'Collection: ' + enterprise.title);

  const objectsRaw = safeGet(
    baseUrl + 'collections/' + enterprise.id + '/objects/?match[type]=attack-pattern&limit=20',
    taxiiHeaders
  );

  if (!objectsRaw) { appendItem(body, '⚠ Could not fetch ATT&CK techniques.'); return; }

  let objects;
  try { objects = JSON.parse(objectsRaw); } catch(e) {
    appendItem(body, '⚠ ATT&CK parse error: ' + e.message); return;
  }

  (objects.objects || []).slice(0, 20).forEach(function(obj) {
    const name    = obj.name || 'Unnamed';
    const desc    = (obj.description || '').replace(/\n/g, ' ').substring(0, 250);
    const tactics = (obj.kill_chain_phases || []).map(function(p){ return p.phase_name; }).join(', ');
    const extRef  = (obj.external_references || []).find(function(r){ return r.source_name === 'mitre-attack'; });
    const extId   = extRef ? extRef.external_id : '';
    appendItem(body,
      '[' + (extId || 'ATT&CK') + ' · ' + (tactics || 'no tactic') + '] '
      + name + '\n  ' + desc
    );
  });
}


