// SECTION 6 — arXiv: Cybersecurity Research Papers
// ============================================================
function fetchArXiv(body) {
  appendSectionHeader(body, '6 · arXiv — Latest Cybersecurity Research Papers');

  const searches = [
    { q: 'cat:cs.CR+AND+ransomware',                   label: 'Ransomware' },
    { q: 'cat:cs.CR+AND+intrusion+detection',          label: 'IDS/IPS' },
    { q: 'cat:cs.CR+AND+malware+classification',       label: 'Malware Analysis' },
    { q: 'cat:cs.CR+AND+vulnerability+detection',      label: 'Vulnerability' },
    { q: 'cat:cs.CR+AND+adversarial+machine+learning', label: 'Adversarial ML' },
    { q: 'cat:cs.CR+AND+phishing',                     label: 'Phishing' },
  ];

  searches.forEach(function(s) {
    const url = 'http://export.arxiv.org/api/query'
      + '?search_query=' + s.q
      + '&start=0&max_results=2&sortBy=submittedDate&sortOrder=descending';

    const raw = safeGet(url);
    if (!raw) return;

    const entries = raw.match(/<entry>([\s\S]*?)<\/entry>/g) || [];
    entries.forEach(function(entry) {
      const title     = ((entry.match(/<title>([\s\S]*?)<\/title>/)     || [])[1] || '').trim();
      const summary   = ((entry.match(/<summary>([\s\S]*?)<\/summary>/) || [])[1] || '').trim();
      const published = ((entry.match(/<published>(.*?)<\/published>/)  || [])[1] || '').substring(0, 10);
      const authorMatches = entry.match(/<name>(.*?)<\/name>/g) || [];
      const authors = authorMatches
        .slice(0, 3)
        .map(function(a){ return a.replace(/<\/?name>/g, ''); })
        .join(', ');

      if (title && title !== 'Error') {
        appendItem(body,
          '[arXiv · ' + s.label + ' · ' + published + '] ' + title
          + '\n  Authors: ' + (authors || 'Unknown')
          + '\n  Abstract: ' + summary.replace(/\n/g, ' ').substring(0, 300) + '...'
        );
      }
    });
  });
}


