// SECTION 1 — NIST NVD: Recent CVEs
// ============================================================
function fetchNistCVEs(body) {
  appendSectionHeader(body, '1 · NIST NVD — Recent CVEs (Last ' + CONFIG.CVE_LOOKBACK_DAYS + ' Days)');

  const endDate   = getNistDate(0);
  const startDate = getNistDate(CONFIG.CVE_LOOKBACK_DAYS);

  const url = 'https://services.nvd.nist.gov/rest/json/cves/2.0'
    + '?pubStartDate=' + startDate
    + '&pubEndDate='   + endDate
    + '&resultsPerPage=' + CONFIG.MAX_CVE_RESULTS;

  const raw = safeGet(url, {
    'User-Agent': 'CyberSecAggregator/3.0 (Google Apps Script)',
    'Accept': 'application/json'
  });

  if (!raw) {
    appendItem(body, '⚠ Could not fetch NIST CVE data. May be rate-limiting — try again in 30 seconds.');
    return;
  }

  let data;
  try { data = JSON.parse(raw); } catch(e) {
    appendItem(body, '⚠ CVE parse error: ' + e.message);
    return;
  }

  const vulns = data.vulnerabilities || [];
  if (vulns.length === 0) {
    appendItem(body, 'No new CVEs published in the last ' + CONFIG.CVE_LOOKBACK_DAYS + ' days.');
    return;
  }

  vulns.forEach(function(v) {
    const cve      = v.cve;
    const id       = cve.id || 'UNKNOWN';
    const descObj  = (cve.descriptions || []).find(function(d){ return d.lang === 'en'; });
    const descText = descObj ? descObj.value.substring(0, 280) : 'No description available';

    let score = 'N/A';
    if (cve.metrics) {
      if (cve.metrics.cvssMetricV31 && cve.metrics.cvssMetricV31[0])
        score = cve.metrics.cvssMetricV31[0].cvssData.baseScore;
      else if (cve.metrics.cvssMetricV30 && cve.metrics.cvssMetricV30[0])
        score = cve.metrics.cvssMetricV30[0].cvssData.baseScore;
      else if (cve.metrics.cvssMetricV2 && cve.metrics.cvssMetricV2[0])
        score = cve.metrics.cvssMetricV2[0].cvssData.baseScore;
    }

    const severity = score !== 'N/A' ? getSeverityLabel(parseFloat(score)) : '';
    appendItem(body, '[' + id + '] CVSS ' + score + ' ' + severity + '\n  ' + descText);
  });
}


