// SECTION 3 — CISA KEV: Actively Exploited Vulnerabilities
// ============================================================
function fetchCISAKEV(body) {
  appendSectionHeader(body, '3 · CISA KEV — Actively Exploited Vulnerabilities');

  const rssRaw = safeGet('https://kevin.gtfkd.com/rss');
  if (rssRaw) {
    const items = rssRaw.match(/<item>([\s\S]*?)<\/item>/g) || [];
    if (items.length > 0) {
      items.slice(0, 15).forEach(function(item) {
        const title = (
          item.match(/<title><!\[CDATA\[(.*?)\]\]><\/title>/) ||
          item.match(/<title>(.*?)<\/title>/) || []
        )[1] || '';

        const rawDesc = (
          item.match(/<description><!\[CDATA\[([\s\S]*?)\]\]><\/description>/) ||
          item.match(/<description>([\s\S]*?)<\/description>/) || []
        )[1] || '';

        const decoded = decodeHtmlEntities(rawDesc);
        const clean   = stripHtmlTags(decoded).replace(/\s+/g, ' ').trim().substring(0, 220);
        appendItem(body, (title ? title + ' — ' : '') + clean);
      });
      return;
    }
  }

  // Fallback: CISA direct JSON
  const jsonRaw = safeGet('https://www.cisa.gov/sites/default/files/feeds/known_exploited_vulnerabilities.json');
  if (!jsonRaw) { appendItem(body, '⚠ Could not fetch CISA KEV data.'); return; }

  let data;
  try { data = JSON.parse(jsonRaw); } catch(e) {
    appendItem(body, '⚠ CISA parse error: ' + e.message);
    return;
  }

  const vulns = (data.vulnerabilities || []).slice(-15).reverse();
  vulns.forEach(function(v) {
    appendItem(body,
      '[' + v.cveID + '] ' + v.vulnerabilityName
      + '\n  ' + (v.shortDescription || '').substring(0, 220)
    );
  });
}


