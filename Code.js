// SECTION 4 — GDELT: Global Cybersecurity Media Events
// ============================================================
function fetchGDELT(body) {
  appendSectionHeader(body, '4 · GDELT — Global Cybersecurity Media Events');

  const queries = [
    'cybersecurity+hacking',
    'ransomware+attack',
    'data+breach+malware',
    'nation+state+cyber',
  ];

  let totalFetched = 0;

  queries.forEach(function(q) {
    if (totalFetched >= 16) return;

    const url = 'https://api.gdeltproject.org/api/v1/search_ftxtsearch/search_ftxtsearch'
      + '?query=' + q
      + '&output=artlist'
      + '&sort=datedesc'
      + '&maxrecords=4';

    const raw = safeGet(url);
    if (!raw) return;

    const lines = raw.split('\n').filter(function(l){ return l.trim().length > 0; });
    lines.slice(0, 4).forEach(function(line) {
      const parts = line.split('\t');
      if (parts.length >= 3) {
        const date   = (parts[0] || '').substring(0, 8);
        const title  = (parts[2] || parts[1] || 'Untitled').trim();
        const domain = (parts[3] || '').trim();
        if (title && title.length > 5) {
          appendItem(body, '[' + date + '] ' + title + (domain ? ' (' + domain + ')' : ''));
          totalFetched++;
        }
      }
    });
  });

  if (totalFetched === 0) {
    appendItem(body, '⚠ GDELT returned no results. This is intermittent — will retry on next run.');
  }
}


