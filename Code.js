// SECTION 2 — NIST CVE Change History
// ============================================================
function fetchCVEHistory(body) {
  appendSectionHeader(body, '2 · NIST NVD — CVE Change History (Last 3 Days)');

  const endDate   = getNistDate(0);
  const startDate = getNistDate(3);

  const url = 'https://services.nvd.nist.gov/rest/json/cvehistory/2.0'
    + '?changeStartDate=' + startDate
    + '&changeEndDate='   + endDate
    + '&resultsPerPage=15';

  const raw = safeGet(url, {
    'User-Agent': 'CyberSecAggregator/3.0 (Google Apps Script)',
    'Accept': 'application/json'
  });

  if (!raw) {
    appendItem(body, '⚠ Could not fetch CVE history. May be rate-limited — wait 30s and retry.');
    return;
  }

  let data;
  try { data = JSON.parse(raw); } catch(e) {
    appendItem(body, '⚠ History parse error: ' + e.message);
    return;
  }

  const changes = data.cveChanges || [];
  if (changes.length === 0) {
    appendItem(body, 'No CVE changes in the last 3 days.');
    return;
  }

  changes.forEach(function(c) {
    const change  = c.change;
    const id      = change.cveId || 'UNKNOWN';
    const types   = (change.details || []).map(function(d){ return d.type; }).join(', ') || 'Unknown';
    const created = (change.created || '').substring(0, 10);
    appendItem(body, '[' + id + '] Changed: ' + types + ' on ' + created);
  });
}


