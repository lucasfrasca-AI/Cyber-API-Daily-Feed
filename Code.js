function fetchGoogleNewsRSS(body) {
  appendSectionHeader(body, '5 · Google News RSS — Cybersecurity Headlines');

  const queries = [
    { term: 'cybersecurity+attack',      label: 'General' },
    { term: '"data+breach"',             label: 'Data Breach' },
    { term: 'ransomware',                label: 'Ransomware' },
    { term: '"zero+day"+vulnerability',  label: 'Zero-Day' },
    { term: 'malware+attack',            label: 'Malware' },
    { term: 'APT+nation+state+hacking',  label: 'Nation-State' },
  ];

  queries.forEach(function(q) {
    const url = 'https://news.google.com/rss/search?q=' + q.term + '&hl=en-AU&gl=AU&ceid=AU:en';
    const raw = safeGet(url);
    if (!raw) return;

    const items = raw.match(/<item>([\s\S]*?)<\/item>/g) || [];
    items.slice(0, 4).forEach(function(item) {
      const title   = (item.match(/<title>(.*?)<\/title>/)     || [])[1] || '';
      const pubDate = (item.match(/<pubDate>(.*?)<\/pubDate>/) || [])[1] || '';
      const clean   = title.replace(/<[^>]+>/g, '').replace(/&amp;/g, '&').trim();
      if (clean) appendItem(body, '[' + q.label + ' · ' + pubDate.substring(0, 16) + '] ' + clean);
    });
  });
}


