// SECTION 10 — Hacker News: Community Security Stories
// ============================================================
function fetchHackerNews(body) {
  appendSectionHeader(body, '10 · Hacker News — Community Cybersecurity Stories');

  const hnRaw = safeGet('https://hacker-news.firebaseio.com/v0/topstories.json');
  if (!hnRaw) {
    appendItem(body, '⚠ Could not reach Hacker News.');
    return;
  }

  let ids;
  try { ids = JSON.parse(hnRaw).slice(0, 150); } catch(e) {
    appendItem(body, '⚠ Hacker News parse error: ' + e.message);
    return;
  }

  const cyberKeywords = [
    'security', 'hack', 'breach', 'malware', 'ransomware',
    'vulnerability', 'exploit', 'cyber', 'phish', 'cve',
    'zero-day', 'zero day', 'botnet', 'spyware', 'trojan',
    'attack', 'encryption', 'firewall', 'backdoor',
    'surveillance', 'apt', 'nation state', 'data leak'
  ];

  let cyberCount = 0;

  for (let i = 0; i < ids.length && cyberCount < 12; i++) {
    const storyRaw = safeGet('https://hacker-news.firebaseio.com/v0/item/' + ids[i] + '.json');
    if (!storyRaw) continue;

    let story;
    try { story = JSON.parse(storyRaw); } catch(e) { continue; }

    const titleLower = (story.title || '').toLowerCase();
    const isCyber = cyberKeywords.some(function(kw){ return titleLower.indexOf(kw) !== -1; });

    if (isCyber && story.title) {
      const date = story.time
        ? new Date(story.time * 1000).toISOString().substring(0, 10)
        : 'Unknown date';
      appendItem(body,
        '[Hacker News · ' + date + '] ' + story.title
        + (story.score       ? ' — ' + story.score + ' pts'            : '')
        + (story.descendants ? ' · ' + story.descendants + ' comments' : '')
        + (story.url         ? '\n  ' + story.url                      : '')
      );
      cyberCount++;
    }
    Utilities.sleep(80);
  }

  if (cyberCount === 0) {
    appendItem(body, 'No cybersecurity stories found in Hacker News top 150 right now.');
  }
}


