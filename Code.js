function fetchYouTube(body) {
  appendSectionHeader(body, '8 · YouTube — Cybersecurity Videos & Conference Talks');

  if (!CONFIG.GOOGLE_API_KEY || CONFIG.GOOGLE_API_KEY === 'YOUR_NEW_GOOGLE_API_KEY') {
    appendItem(body, '⚠ Google API key not set. Create one at console.cloud.google.com and enable YouTube Data API v3.');
    return;
  }

  const ninetyDaysAgo = new Date();
  ninetyDaysAgo.setDate(ninetyDaysAgo.getDate() - 90);
  const publishedAfter = ninetyDaysAgo.toISOString();

  const searches = [
    { q: 'cyber threat intelligence briefing 2026',     label: 'Threat Intel' },
    { q: 'DEF CON Black Hat 2025 talk presentation',    label: 'Conference' },
    { q: 'malware analysis reverse engineering 2025',    label: 'Malware' },
    { q: 'incident response SOC walkthrough 2025 2026', label: 'Incident Response' },
    { q: 'cybersecurity news weekly briefing 2026',     label: 'News Roundup' },
  ];

  const blockWords = [
    'hiring', 'freshers', 'salary', 'job opening', 'placement',
    'recruitment', 'course promo', 'get rich', 'beginners guide',
    'roadmap', '5 levels', '8 minutes', 'anyone can',
    'your phone is hacked', 'dark web explained', 'wifi hack',
    'spy on', 'track your', 'spy and track', '$50 device',
    'ethical hacker explains', 'top 10 hacks', 'every level',
    'full course', 'bootcamp',
  ];

  let totalResults = 0;

  searches.forEach(function(s) {
    if (totalResults >= CONFIG.MAX_YOUTUBE_RESULTS) return;

    const url = 'https://www.googleapis.com/youtube/v3/search'
      + '?part=snippet'
      + '&q=' + encodeURIComponent(s.q)
      + '&type=video'
      + '&videoDuration=medium'
      + '&relevanceLanguage=en'
      + '&order=date'
      + '&publishedAfter=' + publishedAfter
      + '&maxResults=5'
      + '&key=' + CONFIG.GOOGLE_API_KEY;

    const raw = safeGet(url);
    if (!raw) return;

    let data;
    try { data = JSON.parse(raw); } catch(e) { return; }

    if (data.error) {
      appendItem(body, '⚠ YouTube API error: ' + data.error.message);
      return;
    }

    (data.items || []).forEach(function(item) {
      if (totalResults >= CONFIG.MAX_YOUTUBE_RESULTS) return;

      const snip    = item.snippet || {};
      const title   = snip.title        || 'Untitled';
      const channel = snip.channelTitle || 'Unknown';
      const pub     = (snip.publishedAt || '').substring(0, 10);
      const desc    = (snip.description || '').replace(/\n/g, ' ').substring(0, 160);
      const videoId = (item.id && item.id.videoId) ? item.id.videoId : '';
      const ytUrl   = videoId ? 'https://youtu.be/' + videoId : '';

      const titleLower = title.toLowerCase();
      const isBlocked  = blockWords.some(function(w){ return titleLower.indexOf(w) !== -1; });
      if (isBlocked || title.length < 15) return;

      appendItem(body,
        '[YouTube · ' + s.label + ' · ' + pub + '] "' + title + '" by ' + channel
        + '\n  ' + desc
        + (ytUrl ? '\n  ' + ytUrl : '')
      );
      totalResults++;
    });
  });

  if (totalResults === 0) {
    appendItem(body, '⚠ No relevant YouTube videos found. Check YouTube Data API v3 is enabled in Google Cloud Console.');
  }
}


