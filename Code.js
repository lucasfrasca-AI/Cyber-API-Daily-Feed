// UTILITY FUNCTIONS
// ============================================================

function getOrCreateDoc(name) {
  const files = DriveApp.getFilesByName(name);
  if (files.hasNext()) return DocumentApp.openById(files.next().getId());
  return DocumentApp.create(name);
}

function appendSectionHeader(body, title) {
  body.appendParagraph('').setHeading(DocumentApp.ParagraphHeading.NORMAL);
  const p = body.appendParagraph(title);
  p.setHeading(DocumentApp.ParagraphHeading.HEADING2);
  p.setBold(true);
}

function appendItem(body, text) {
  body.appendParagraph('• ' + text).setHeading(DocumentApp.ParagraphHeading.NORMAL);
}

function safeGet(url, headers) {
  try {
    const options = { muteHttpExceptions: true, headers: headers || {} };
    const response = UrlFetchApp.fetch(url, options);
    const code = response.getResponseCode();
    if (code === 200) return response.getContentText();
    Logger.log('HTTP ' + code + ' for: ' + url);
    return null;
  } catch(e) {
    Logger.log('Exception fetching ' + url + ': ' + e.message);
    return null;
  }
}

function getNistDate(daysAgo) {
  const d = new Date();
  d.setDate(d.getDate() - daysAgo);
  return d.toISOString().replace('Z', '').substring(0, 23);
}

function getSimpleDate(daysAgo) {
  const d = new Date();
  d.setDate(d.getDate() - daysAgo);
  return d.toISOString().substring(0, 10);
}

function getSeverityLabel(score) {
  if (score >= 9.0) return '🔴 CRITICAL';
  if (score >= 7.0) return '🟠 HIGH';
  if (score >= 4.0) return '🟡 MEDIUM';
  return '🟢 LOW';
}

function decodeHtmlEntities(text) {
  return text
    .replace(/&lt;/g,   '<')
    .replace(/&gt;/g,   '>')
    .replace(/&amp;/g,  '&')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g,  "'")
    .replace(/&nbsp;/g, ' ');
}

function stripHtmlTags(text) {
  return text.replace(/<[^>]+>/g, '');
}


