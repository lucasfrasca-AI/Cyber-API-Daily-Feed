function fetchMediaStack(body) {
  appendSectionHeader(body, '7 · MediaStack — Cybersecurity News Articles');

  if (CONFIG.MEDIASTACK_KEY === 'ae*****************') {
    appendItem(body, '⚠ MediaStack key not set — paste your key into CONFIG.MEDIASTACK_KEY at the top of the script.');
    return;
  }

  const msUrl = 'https://api.mediastack.com/v1/news'
    + '?access_key=' + CONFIG.MEDIASTACK_KEY
    + '&keywords=cybersecurity,hacking,ransomware,malware,data breach,zero day'
    + '&languages=en'
    + '&limit=20'
    + '&sort=published_desc';

  const msRaw = safeGet(msUrl);

  if (!msRaw) {
    appendItem(body, '⚠ Could not reach MediaStack. Check your key at mediastack.com.');
    return;
  }

  let msData;
  try { msData = JSON.parse(msRaw); } catch(e) {
    appendItem(body, '⚠ MediaStack parse error: ' + e.message);
    return;
  }

  if (msData.error) {
    appendItem(body, '⚠ MediaStack error [' + msData.error.code + ']: ' + msData.error.message);
    return;
  }

  const articles = msData.data || [];
  if (articles.length === 0) {
    appendItem(body, 'No MediaStack articles returned for this period.');
    return;
  }

  articles.forEach(function(a) {
    const source = a.source        || 'Unknown';
    const title  = a.title         || 'Untitled';
    const desc   = (a.description  || '').replace(/\n/g, ' ').substring(0, 180);
    const date   = (a.published_at || '').substring(0, 10);
    const url    = a.url           || '';
    appendItem(body,
      '[' + source + ' · ' + date + '] ' + title
      + (desc ? '\n  ' + desc : '')
      + (url  ? '\n  ' + url  : '')
    );
  });
}


